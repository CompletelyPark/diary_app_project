package com.jinwan.appproject.schedule;

public class Schedule2 {

    private String memo2;
    private String timeFirst2;
    private String timeLast2;

    public Schedule2(String memo2, String timeFirst2, String timeLast2) {
        this.memo2 = memo2;
        this.timeFirst2 = timeFirst2;
        this.timeLast2 = timeLast2;
    }

    public String getMemo2() {
        return memo2;
    }

    public String getTimeFirst2() {
        return timeFirst2;
    }

    public String getTimeLast2() {
        return timeLast2;
    }

}
